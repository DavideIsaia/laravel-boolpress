    private function generateSlugFromTitle($title) {
        $base_slug = Str::slug($title, '-');
        $slug = $base_slug;
        $i = 1;
        $post_same_name = Post::where('slug', '=', $slug)->first();
        while ($post_same_name) {
            $slug = $base_slug . '-' . $i;
            $post_same_name = Post::where('slug', '=', $slug)->first();
            $i++;
        }
        return $slug;
    }
